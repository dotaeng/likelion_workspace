let weather = 'sunny';

switch(weather){
case weather = 'windy':
    console.log('오늘은 바람이 많이 분다.');
    break;
case weather = 'rainy':
    console.log('오늘은 비가 온다.');
    break;
case weather = 'sunny':
    console.log('오늘은 날씨가 좋다.');
    break;
default:
    console.log('오늘의 날씨는 어떤가요?');
}